# 다양한 리스트 생성
# --------------------------------------------------------
# 실수 데이터로 구성된 리스트 생성
floatNums = [4., 3.1, 6.3, 5.01]

# str 데이터로 구성된 리스트 생성
strNums = ['44', '56', '98']

# bool 데이터로 구성된 리스트 생성
boolNums = [False, False, True, True, True]

# 다양한 데이터 타입으로 구성된 리스트 생성
var_nums = ['100', 98, False, 7.12, 'Apple', True]

# 빈 리스트 생성
empty_list = []

# 리스트 데이터로 구성된 리스트 생성
multi_list = [10, 20, 30, ['A', 'B'], 200, 100]

# 리스트의 요소 출력
print(f'multi_list[0] -> {multi_list[0]} {type(multi_list[0])}')
print(f'multi_list[1] -> {multi_list[1]} {type(multi_list[1])}')
print(f'multi_list[2] -> {multi_list[2]} {type(multi_list[2])}')
print(f'multi_list[3] -> {multi_list[3]} {type(multi_list[3])}')
print(f'multi_list[4] -> {multi_list[4]} {type(multi_list[4])}')
print(f'multi_list[5] -> {multi_list[5]} {type(multi_list[5])}')

print(f'multi_list[3][0] -> {multi_list[3][0]} {type(multi_list[3][0])}')
print(f'multi_list[3][1] -> {multi_list[3][1]} {type(multi_list[3][1])}')

